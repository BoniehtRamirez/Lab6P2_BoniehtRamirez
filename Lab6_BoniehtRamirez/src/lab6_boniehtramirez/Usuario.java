package lab6_boniehtramirez;

public class Usuario {
    private String nombre, apellido, numeroRegistro, usuario, contraseña, facultad;

    public Usuario(String nombre, String apellido, String numeroRegistro, String usuario, String contraseña, String facultad) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.numeroRegistro = numeroRegistro;
        this.usuario = usuario;
        this.contraseña = contraseña;
        this.facultad = facultad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getNumeroRegistro() {
        return numeroRegistro;
    }

    public void setNumeroRegistro(String numeroRegistro) {
        this.numeroRegistro = numeroRegistro;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getContraseña() {
        return contraseña;
    }

    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }

    public String getFacultad() {
        return facultad;
    }

    public void setFacultad(String facultad) {
        this.facultad = facultad;
    }

    
    
}
